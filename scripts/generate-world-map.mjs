/**
 * Generates the region-tinted world map used on /destinations.
 *
 * Natural Earth 110m country geometry (public domain, via the `world-atlas`
 * package) is projected once here and written out as a static TS module, so the
 * site ships plain path data with no runtime mapping dependency. Re-run with
 *   node scripts/generate-world-map.mjs
 * after changing the region mapping below.
 *
 * Countries are assigned to the seven regions the site actually sells, which
 * are editorial rather than strictly UN M49 — Cyprus, Turkey and Russia sit
 * under Europe here because that is how the destinations are filed.
 */
import { feature } from "topojson-client";
import { geoNaturalEarth1, geoPath } from "d3-geo";
import { writeFileSync } from "node:fs";
import { createRequire } from "node:module";

const require = createRequire(import.meta.url);
const topo = require("world-atlas/countries-110m.json");

const REGIONS = {
  africa: [12,24,204,72,854,108,120,132,140,148,174,178,180,384,262,818,226,232,748,231,266,270,288,324,624,404,426,430,434,450,454,466,478,480,504,508,516,562,566,646,678,686,690,694,706,710,728,729,834,768,788,800,894,716,732],
  europe: [8,20,40,112,56,70,100,191,196,203,208,233,246,250,276,300,348,352,372,380,428,438,440,442,470,498,492,499,528,807,578,616,620,642,643,674,688,703,705,724,752,756,792,804,826,336,234,248,999,998],
  "middle-east": [51,31,48,268,364,368,376,400,414,422,512,275,634,682,760,784,887],
  asia: [4,50,64,116,156,158,344,356,360,392,398,417,418,458,462,496,104,524,408,410,586,608,702,144,762,764,626,795,860,704,446,96,242,36,554,598,90,548,776,882,184,258,585,583,520,540,570,798,876,316,584,296,798],
  "north-america": [124,840,304,60,666],
  "caribbean-central-america": [84,188,222,320,340,484,558,591,44,52,192,212,214,308,332,388,630,659,662,670,780,28,533,531],
  "south-america": [32,68,76,152,170,218,238,254,328,600,604,740,858,862],
};

const regionOf = new Map();
// Natural Earth ids are zero-padded ISO numerics ("012"), so normalise to Number.
for (const [slug, ids] of Object.entries(REGIONS)) for (const id of ids) regionOf.set(Number(id), slug);

// Natural Earth gives these non-ISO ids; file them where the site would.
const BY_NAME = { "Kosovo": "europe", "N. Cyprus": "europe", "Somaliland": "africa" };
const fc = feature(topo, topo.objects.countries);
const W = 1000, H = 500;
const projection = geoNaturalEarth1().fitExtent([[6, 6], [W - 6, H - 6]], fc);
const path = geoPath(projection);

const round = (d) => d.replace(/-?\d+\.\d+/g, (n) => (+n).toFixed(1));

const countries = [];
for (const f of fc.features) {
  if (f.properties?.name === "Antarctica") continue;
  const d = path(f);
  if (!d) continue;
  countries.push({ id: String(f.id), name: f.properties?.name ?? "", region: regionOf.get(Number(f.id)) ?? BY_NAME[f.properties?.name] ?? null, d: round(d) });
}

// Label anchor per region: centroid of its combined geometry.
const labels = {};
for (const slug of Object.keys(REGIONS)) {
  const feats = fc.features.filter((f) => (regionOf.get(Number(f.id)) ?? BY_NAME[f.properties?.name]) === slug);
  if (!feats.length) continue;
  const [x, y] = path.centroid({ type: "FeatureCollection", features: feats });
  if (Number.isFinite(x)) labels[slug] = [Math.round(x), Math.round(y)];
}

const out = `// GENERATED by scripts/generate-world-map.mjs — do not edit by hand.
// Natural Earth 110m via world-atlas (public domain), projected with geoNaturalEarth1.
export const WORLD_VIEWBOX = "0 0 ${W} ${H}";
export type WorldCountry = { id: string; name: string; region: string | null; d: string };
export const WORLD_COUNTRIES: WorldCountry[] = ${JSON.stringify(countries)};
export const REGION_LABEL_POINTS: Record<string, [number, number]> = ${JSON.stringify(labels)};
`;
writeFileSync("src/content/world-map.generated.ts", out);

const assigned = countries.filter((c) => c.region).length;
console.log(`countries: ${countries.length}  assigned to a region: ${assigned}  unassigned: ${countries.length - assigned}`);
console.log("labels:", Object.keys(labels).join(", "));
console.log(`output: ${(out.length / 1024).toFixed(0)} KB`);
